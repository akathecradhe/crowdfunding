CREATE TABLE `fundraiser` (
  `id` INT NOT NULL,
  `BANKDETAILS` VARCHAR(45) NULL,
  `EMAIL` VARCHAR(45) NULL,
  `LOCATION` VARCHAR(45) NULL,
  `NAME` VARCHAR(45) NULL,
  `PURPOSE` VARCHAR(500) NULL,
  `targetAmount` INT NOT NULL,
  `targetDate` DATE NOT NULL,
  `currentAmount` INT ,
  PRIMARY KEY (`id`));